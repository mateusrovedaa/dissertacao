t1 = time.time()

def swinging_door_algorithm(data, deviation):
    n = len(data)
    result = [(data[0][0], data[0][1])]
    left = 0

    for right in range(1, n):
        if abs(data[right][1] - data[left][1]) > deviation:
            result.append((data[right][0], data[right][1]))
            left = right

    return result

